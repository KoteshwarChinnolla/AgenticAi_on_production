class tools:
    def add(self,a:int,b:int)->int:
        """ Add a and b.

        Args :
        a: first int
        b: second int
        """
        return a+b

    def Subtract(self,a:int,b:int)->int:
        """ Subtract b from a

        Args :
        a: first int
        b: second int
        """
        return a-b
    
    def multiply(self,a: int , b:int)-> int:
        """ Multiply a and b

        Args :
        a: first int
        b: second int
        """
        return a*b

    def divide(self,a :int, b:int)->float:

        """ Divide a by b

        Args :
        a: first int
        b: second int
        """
        return a/b

    