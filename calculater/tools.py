class tools:
    def add(self,a:float,b:float)->float:
        """ Add a and b.

        Args :
        a: first float
        b: second float
        """
        return a+b

    def subtract(self,a:float,b:float)->float:
        """ subtract b from a

        Args :
        a: first float
        b: second float
        """
        return a-b
    
    def multiply(self,a: float , b:float)-> float:
        """ Multiply a and b

        Args :
        a: first float
        b: second float
        """
        return a*b

    def divide(self,a :float, b:float)->float:

        """ Divide a by b

        Args :
        a: first float
        b: second float
        """
        return a/b

    def power(self,a:float,b:float)->float:
        """ Raise a to the power of b

        Args :
        a: first float
        b: second float
        """
        return a**b